    async def select_playbook_for_alert(
        self,
        alert: Dict[str, Any]
    ) -> PlaybookSelection:
        """
        Automatically select the best playbook for an alert.

        Selection algorithm:
        1. Filter by is_enabled=True AND riggs_allowed=True
        2. Match by alert_types (exact match)
        3. Match by tags (fuzzy match on alert content)
        4. Match by severity_filter
        5. Match by data_sources
        6. Sort by priority (higher wins)
        7. If multiple matches, use riggs_confidence
        """
        try:
            from services.postgres_db import postgres_db

            if not postgres_db.connected:
                return PlaybookSelection()

            async with postgres_db.tenant_acquire() as conn:
                # Get all enabled playbooks that Riggs can use
                rows = await conn.fetch('''
                    SELECT * FROM playbooks
                    WHERE is_enabled = TRUE
                    ORDER BY priority DESC, riggs_confidence DESC NULLS LAST
                ''')

                if not rows:
                    return PlaybookSelection(match_reasons=["No enabled playbooks found"])

                # Extract alert metadata
                alert_type = alert.get('type', alert.get('alert_type', 'unknown'))
                alert_severity = alert.get('severity', 'medium')
                alert_source = alert.get('source', alert.get('data_source', ''))
                alert_tags = self._extract_alert_tags(alert)

                candidates = []

                for row in rows:
                    playbook = self._row_to_dict(row)
                    score = 0
                    reasons = []

                    # Check alert_types match
                    pb_alert_types = playbook.get('alert_types', [])
                    if pb_alert_types:
                        if alert_type.lower() in [t.lower() for t in pb_alert_types]:
                            score += 40
                            reasons.append(f"Alert type '{alert_type}' matches")
                        elif any(t.lower() in alert_type.lower() for t in pb_alert_types):
                            score += 20
                            reasons.append(f"Alert type partially matches")

                    # Check tags match
                    pb_tags = playbook.get('tags', [])
                    if pb_tags and alert_tags:
                        matching_tags = set(pb_tags) & set(alert_tags)
                        if matching_tags:
                            score += len(matching_tags) * 10
                            reasons.append(f"Tags match: {', '.join(matching_tags)}")

                    # Check severity filter
                    pb_severities = playbook.get('severity_filter', [])
                    if pb_severities:
                        if alert_severity.lower() in [s.lower() for s in pb_severities]:
                            score += 15
                            reasons.append(f"Severity '{alert_severity}' matches filter")

                    # Check data source
                    pb_sources = playbook.get('data_sources', [])
                    if pb_sources and alert_source:
                        if any(s.lower() in alert_source.lower() for s in pb_sources):
                            score += 10
                            reasons.append(f"Data source matches")

                    # Boost by priority
                    score += (playbook.get('priority', 50) / 10)

                    # Boost by Riggs confidence
                    if playbook.get('riggs_confidence'):
                        score += playbook['riggs_confidence'] * 5

                    if score > 0:
                        candidates.append({
                            "playbook": playbook,
                            "score": score,
                            "reasons": reasons
                        })

                # Sort by score
                candidates.sort(key=lambda x: x['score'], reverse=True)

                if not candidates:
                    return PlaybookSelection(
                        match_reasons=["No matching playbooks found"],
                        should_build_new=True
                    )

                # Best match
                best = candidates[0]
                selected = best['playbook']

                # Check if can auto-execute
                can_auto = (
                    selected.get('riggs_allowed', False) and
                    not selected.get('requires_approval', True)
                )

                return PlaybookSelection(
                    selected_playbook=selected,
                    match_score=best['score'],
                    match_reasons=best['reasons'],
                    alternatives=[c['playbook'] for c in candidates[1:5]],
                    requires_approval=selected.get('requires_approval', True),
                    can_auto_execute=can_auto
                )

        except Exception as e:
            logger.error(f"Failed to select playbook: {e}")
            return PlaybookSelection(match_reasons=[f"Error: {str(e)}"])

    async def can_run_autonomously(
        self,
        playbook_id: str,
        alert: Dict[str, Any]
    ) -> AutonomyCheck:
        """
        Check if Riggs can run this playbook without human intervention.
        """
        try:
            from services.postgres_db import postgres_db

            blocking_reasons = []

            async with postgres_db.tenant_acquire() as conn:
                playbook = await conn.fetchrow(
                    "SELECT * FROM playbooks WHERE id = $1",
                    uuid.UUID(playbook_id)
                )

                if not playbook:
                    return AutonomyCheck(
                        can_run=False,
                        blocking_reasons=["Playbook not found"]
                    )

                # Check enabled
                if not playbook['is_enabled']:
                    blocking_reasons.append("Playbook is not enabled")

                # Check riggs_allowed
                if not playbook['riggs_allowed']:
                    blocking_reasons.append("Playbook does not allow Riggs execution")

                # Check requires_approval
                requires_approval = playbook['requires_approval']

                # Check severity threshold (critical/high can bypass approval)
                alert_severity = alert.get('severity', 'medium').lower()
                auto_approve_severities = ['critical', 'high']

                # Check integrations are configured
                canvas_data = playbook.get('canvas_data')
                if canvas_data:
                    if isinstance(canvas_data, str):
                        import json
                        canvas_data = json.loads(canvas_data)

                    # Extract integration IDs from nodes
                    nodes = canvas_data.get('nodes', [])
                    required_integrations = set()
                    for node in nodes:
                        node_data = node.get('data', {})
                        config = node_data.get('config', {})
                        integration_id = config.get('integration_instance_id')
                        if integration_id:
                            required_integrations.add(integration_id)

                    # Check if required integrations are configured
                    if required_integrations:
                        configured = await conn.fetch(
                            """SELECT id, name, is_enabled FROM integration_instances
                               WHERE id = ANY($1::uuid[])""",
                            list(required_integrations)
                        )
                        configured_ids = {str(row['id']) for row in configured}
                        enabled_ids = {str(row['id']) for row in configured if row['is_enabled']}

                        # Check for missing integrations
                        missing = required_integrations - configured_ids
                        if missing:
                            blocking_reasons.append(f"Missing integrations: {len(missing)} required integrations not configured")

                        # Check for disabled integrations
                        disabled = configured_ids - enabled_ids
                        if disabled:
                            blocking_reasons.append(f"Disabled integrations: {len(disabled)} required integrations are disabled")

                can_run = len(blocking_reasons) == 0

                return AutonomyCheck(
                    can_run=can_run,
                    requires_approval=requires_approval,
                    blocking_reasons=blocking_reasons
                )

        except Exception as e:
            logger.error(f"Failed to check autonomy: {e}")
            return AutonomyCheck(
                can_run=False,
                blocking_reasons=[f"Error: {str(e)}"]
            )

    async def auto_execute_playbook(
        self,
        alert: Dict[str, Any],
        playbook_id: str = None
    ) -> Dict[str, Any]:
        """
        Riggs autonomously executes a playbook for an alert.
        """
        try:
            from services.playbook_engine import get_playbook_engine

            # Select playbook if not specified
            if not playbook_id:
                selection = await self.select_playbook_for_alert(alert)
                if not selection.selected_playbook:
                    return {
                        "error": "No suitable playbook found",
                        "reasons": selection.match_reasons
                    }
                playbook_id = selection.selected_playbook['id']

            # Check autonomy
            autonomy = await self.can_run_autonomously(playbook_id, alert)

            if not autonomy.can_run:
                return {
                    "error": "Cannot run autonomously",
                    "reasons": autonomy.blocking_reasons
                }

            if autonomy.requires_approval:
                # Create approval request
                return await self._request_playbook_approval(playbook_id, alert)

            # Execute playbook
            engine = get_playbook_engine()
            result = await engine.start_execution(
                playbook_id=playbook_id,
                trigger_context={
                    "alert": alert,
                    "alert_id": alert.get('id'),
                    "triggered_by": "riggs_auto"
                },
                triggered_by="riggs"
            )

            # Log decision
            await self._log_riggs_decision(
                decision_type="auto_execute",
                playbook_id=playbook_id,
                alert_id=alert.get('id'),
                result=result
            )

            return result

        except Exception as e:
            logger.error(f"Failed to auto-execute: {e}")
            return {"error": str(e)}

