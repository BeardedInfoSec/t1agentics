    async def analyze_alert(self, alert: Dict[str, Any]) -> PlaybookAnalysis:
        """
        Analyze an alert and determine:
        1. What playbooks could handle it
        2. What's missing (integrations, data)
        3. Recommended actions
        4. Whether to run existing playbook or build new one
        """
        selection = await self.select_playbook_for_alert(alert)

        analysis = PlaybookAnalysis()
        analysis.matching_playbooks = [selection.selected_playbook] if selection.selected_playbook else []
        analysis.matching_playbooks.extend(selection.alternatives[:3])

        # Check for missing integrations based on alert type
        alert_type = alert.get('type', '').lower()

        if 'phishing' in alert_type or 'email' in alert_type:
            analysis.missing_integrations.extend([
                'virustotal', 'urlscan', 'email_gateway'
            ])
            analysis.recommended_actions.extend([
                {"action": "enrich_urls", "priority": "high"},
                {"action": "enrich_sender", "priority": "high"},
                {"action": "check_similar_emails", "priority": "medium"}
            ])

        elif 'malware' in alert_type or 'endpoint' in alert_type:
            analysis.missing_integrations.extend([
                'crowdstrike', 'carbon_black', 'virustotal'
            ])
            analysis.recommended_actions.extend([
                {"action": "isolate_host", "priority": "high"},
                {"action": "collect_forensics", "priority": "high"},
                {"action": "scan_hash", "priority": "medium"}
            ])

        elif 'network' in alert_type or 'intrusion' in alert_type:
            analysis.missing_integrations.extend([
                'firewall', 'ids', 'netflow'
            ])
            analysis.recommended_actions.extend([
                {"action": "block_ip", "priority": "high"},
                {"action": "analyze_traffic", "priority": "medium"}
            ])

        # Determine if should build new
        analysis.should_build_new = (
            selection.match_score < 30 or
            not selection.selected_playbook
        )

        analysis.analysis_summary = self._generate_analysis_summary(alert, selection, analysis)

        return analysis

    async def analyze_playbook(self, playbook_id: str) -> Dict[str, Any]:
        """
        Review existing playbook and suggest improvements.
        """
        try:
            from services.postgres_db import postgres_db

            async with postgres_db.tenant_acquire() as conn:
                playbook = await conn.fetchrow(
                    "SELECT * FROM playbooks WHERE id = $1",
                    uuid.UUID(playbook_id)
                )

                if not playbook:
                    return {"error": "Playbook not found"}

                canvas_data = playbook['canvas_data']
                if isinstance(canvas_data, str):
                    canvas_data = json.loads(canvas_data)

                nodes = canvas_data.get('nodes', [])
                edges = canvas_data.get('edges', [])

                suggestions = []

                # Check for error handling
                has_error_handling = any(
                    n.get('type') in ['condition', 'switch'] and
                    'error' in str(n.get('data', {}).get('config', {})).lower()
                    for n in nodes
                )
                if not has_error_handling:
                    suggestions.append(PlaybookRecommendation(
                        type="add_error_handling",
                        description="Add error handling for failed enrichments or actions",
                        reasoning="Playbooks should gracefully handle failures",
                        priority="high",
                        auto_apply=False
                    ))

                # Check for approval gates before actions
                action_nodes = [n for n in nodes if n.get('type') == 'action']
                for action_node in action_nodes:
                    action_config = action_node.get('data', {}).get('config', {})
                    if not action_config.get('requires_approval', True):
                        # Find upstream nodes
                        upstream = [e['source'] for e in edges if e['target'] == action_node['id']]
                        upstream_types = [
                            next((n['type'] for n in nodes if n['id'] == u), None)
                            for u in upstream
                        ]
                        if 'approval_gate' not in upstream_types:
                            suggestions.append(PlaybookRecommendation(
                                type="add_node",
                                description=f"Add approval gate before '{action_node.get('data', {}).get('label', 'action')}'",
                                reasoning="Response actions should have human approval",
                                priority="high",
                                auto_apply=False,
                                changes={"before_node": action_node['id'], "node_type": "approval_gate"}
                            ))

                # Check for missing enrichment
                trigger_node = next((n for n in nodes if n.get('type') == 'trigger'), None)
                enrich_nodes = [n for n in nodes if n.get('type') == 'enrich']
                if trigger_node and len(enrich_nodes) == 0:
                    suggestions.append(PlaybookRecommendation(
                        type="add_node",
                        description="Add enrichment step after trigger",
                        reasoning="Alerts should be enriched before decision making",
                        priority="medium",
                        auto_apply=True,
                        changes={"after_node": trigger_node['id'], "node_type": "enrich"}
                    ))

                # Check for end node
                has_end = any(n.get('type') == 'end' for n in nodes)
                if not has_end:
                    suggestions.append(PlaybookRecommendation(
                        type="add_node",
                        description="Add explicit end node",
                        reasoning="Playbooks should have clear completion points",
                        priority="low",
                        auto_apply=True,
                        changes={"node_type": "end"}
                    ))

                return {
                    "playbook_id": playbook_id,
                    "node_count": len(nodes),
                    "edge_count": len(edges),
                    "suggestions": [s.dict() for s in suggestions],
                    "suggestion_count": len(suggestions)
                }

        except Exception as e:
            logger.error(f"Failed to analyze playbook: {e}")
            return {"error": str(e)}

