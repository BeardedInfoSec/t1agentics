"""
Riggs Playbook Agent

Provides Riggs with full control over playbook creation, modification, and execution.
Key capabilities:
- Automatic playbook selection for alerts (by tags, alert_types, severity)
- Autonomous execution when allowed
- Building playbooks from alerts
- Adding/updating/deleting nodes
- Creating custom functions and lists
- Recommending improvements and integrations
"""

import json
import logging
import uuid
from datetime import datetime
from typing import Dict, Any, List, Optional, Tuple
from pydantic import BaseModel, Field
from enum import Enum

logger = logging.getLogger(__name__)


# ============================================================================
# Models
# ============================================================================

class PlaybookSelection(BaseModel):
    """Result of playbook selection for an alert."""
    selected_playbook: Optional[Dict[str, Any]] = None
    match_score: float = 0.0
    match_reasons: List[str] = Field(default_factory=list)
    alternatives: List[Dict[str, Any]] = Field(default_factory=list)
    requires_approval: bool = True
    can_auto_execute: bool = False


class AutonomyCheck(BaseModel):
    """Result of checking if Riggs can run autonomously."""
    can_run: bool = False
    requires_approval: bool = True
    blocking_reasons: List[str] = Field(default_factory=list)


class PlaybookAnalysis(BaseModel):
    """Analysis of an alert for playbook purposes."""
    matching_playbooks: List[Dict[str, Any]] = Field(default_factory=list)
    missing_integrations: List[str] = Field(default_factory=list)
    recommended_actions: List[Dict[str, Any]] = Field(default_factory=list)
    should_build_new: bool = False
    analysis_summary: str = ""


class PlaybookRecommendation(BaseModel):
    """A recommendation for improving a playbook."""
    type: str  # add_node, update_node, add_edge, add_error_handling, etc.
    description: str
    reasoning: str
    priority: str = "medium"  # high, medium, low
    auto_apply: bool = False
    changes: Dict[str, Any] = Field(default_factory=dict)


class IntegrationRecommendation(BaseModel):
    """A recommendation for adding an integration."""
    integration_name: str
    integration_type: str
    reason: str
    use_case: str
    node_suggestions: List[Dict[str, Any]] = Field(default_factory=list)


class PlaybookDraft(BaseModel):
    """Draft playbook generated by Riggs."""
    name: str
    description: str
    trigger_conditions: Dict[str, Any]
    canvas_data: Dict[str, Any]
    tags: List[str] = Field(default_factory=list)
    alert_types: List[str] = Field(default_factory=list)


