    def _extract_alert_tags(self, alert: Dict[str, Any]) -> List[str]:
        """Extract relevant tags from an alert."""
        tags = []

        # From alert type
        alert_type = alert.get('type', alert.get('alert_type', ''))
        if alert_type:
            tags.extend(alert_type.lower().replace('-', '_').split('_'))

        # From title/description
        title = alert.get('title', '')
        if 'phishing' in title.lower():
            tags.append('phishing')
        if 'malware' in title.lower():
            tags.append('malware')
        if 'ransomware' in title.lower():
            tags.append('ransomware')
        if 'credential' in title.lower():
            tags.append('credential')

        # From severity
        severity = alert.get('severity', '')
        if severity:
            tags.append(severity.lower())

        # Dedupe
        return list(set(tags))

    def _generate_analysis_summary(
        self,
        alert: Dict[str, Any],
        selection: PlaybookSelection,
        analysis: PlaybookAnalysis
    ) -> str:
        """Generate human-readable analysis summary."""
        parts = []

        if selection.selected_playbook:
            parts.append(f"Found matching playbook: {selection.selected_playbook.get('name')}")
            parts.append(f"Match score: {selection.match_score:.0f}/100")
        else:
            parts.append("No existing playbook matches this alert type")

        if analysis.missing_integrations:
            parts.append(f"Recommended integrations: {', '.join(analysis.missing_integrations[:3])}")

        if analysis.should_build_new:
            parts.append("Recommendation: Build a new playbook for this alert type")

        return ". ".join(parts)

    async def _request_playbook_approval(
        self,
        playbook_id: str,
        alert: Dict[str, Any]
    ) -> Dict[str, Any]:
        """Create an approval request for playbook execution."""
        from services.postgres_db import postgres_db
        from datetime import datetime, timedelta

        try:
            async with postgres_db.tenant_acquire() as conn:
                # Get playbook name
                playbook = await conn.fetchrow(
                    "SELECT name FROM playbooks WHERE id = $1",
                    uuid.UUID(playbook_id)
                )
                playbook_name = playbook['name'] if playbook else 'Unknown Playbook'

                # Extract Riggs analysis from alert if available
                riggs_analysis = alert.get('riggs_analysis', {})
                riggs_verdict = riggs_analysis.get('verdict')
                riggs_confidence = riggs_analysis.get('confidence')

                # Generate approval request ID
                approval_id = f"APR-{datetime.utcnow().strftime('%Y%m%d%H%M%S')}-{str(uuid.uuid4())[:8].upper()}"

                # Set expiration to 24 hours from now
                expires_at = datetime.utcnow() + timedelta(hours=24)

                # Build reasoning
                alert_id = alert.get('alert_id', 'unknown')
                alert_title = alert.get('title', 'Security Alert')
                reasoning = f"Riggs recommends executing playbook '{playbook_name}' for alert '{alert_title}' (ID: {alert_id})"
                if riggs_verdict:
                    reasoning += f". Verdict: {riggs_verdict}"
                if riggs_confidence:
                    reasoning += f" (confidence: {riggs_confidence}%)"

                # Insert approval request
                await conn.execute('''
                    INSERT INTO playbook_execution_approvals (
                        id, investigation_id, playbook_id, playbook_name,
                        riggs_verdict, riggs_confidence, riggs_reasoning,
                        status, created_at, expires_at, tenant_id
                    ) VALUES ($1, $2, $3, $4, $5, $6, $7, $8, NOW(), $9, $10)
                ''',
                    approval_id,
                    alert.get('investigation_id', alert.get('alert_id', 'unknown')),
                    uuid.UUID(playbook_id),
                    playbook_name,
                    riggs_verdict,
                    riggs_confidence,
                    reasoning,
                    'pending',
                    expires_at,
                    alert.get('tenant_id')
                )

                logger.info(f"[RIGGS] Created approval request {approval_id} for playbook {playbook_name}")

                return {
                    "status": "pending_approval",
                    "approval_id": approval_id,
                    "playbook_id": playbook_id,
                    "playbook_name": playbook_name,
                    "expires_at": expires_at.isoformat(),
                    "message": f"Playbook '{playbook_name}' requires approval before execution"
                }

        except Exception as e:
            logger.error(f"[RIGGS] Failed to create approval request: {e}")
            return {
                "status": "error",
                "playbook_id": playbook_id,
                "message": f"Failed to create approval request: {str(e)}"
            }

    async def _log_riggs_decision(
        self,
        decision_type: str,
        playbook_id: str,
        alert_id: str,
        result: Dict[str, Any]
    ):
        """Log Riggs decision for audit trail."""
        from services.postgres_db import postgres_db

        logger.info(
            f"[RIGGS_DECISION] type={decision_type} playbook={playbook_id} "
            f"alert={alert_id} success={result.get('success', False)}"
        )

        try:
            async with postgres_db.tenant_acquire() as conn:
                # Build decision summary
                success = result.get('success', False)
                status = 'success' if success else 'failed'
                decision_value = f"playbook_{status}:{playbook_id[:8]}" if playbook_id else status

                # Extract confidence and reasoning from result
                confidence = result.get('confidence')
                reasoning = result.get('reasoning') or result.get('message') or f"Playbook execution {status}"

                # Get investigation_id if available
                investigation_id = result.get('investigation_id')

                # Build evidence/recommendations from result
                evidence = {
                    'playbook_id': playbook_id,
                    'alert_id': alert_id,
                    'execution_result': {
                        'success': success,
                        'execution_id': result.get('execution_id'),
                        'error': result.get('error')
                    }
                }

                recommendations = result.get('recommendations', [])

                # Insert decision record
                from middleware.tenant_middleware import get_optional_tenant_id
                await conn.execute('''
                    INSERT INTO riggs_decisions (
                        investigation_id, decision_type, decision_value,
                        reasoning, confidence, evidence, recommendations,
                        created_by, tenant_id
                    ) VALUES (
                        $1, $2, $3, $4, $5, $6::jsonb, $7::jsonb, 'riggs_playbook_agent', $8
                    )
                ''',
                    uuid.UUID(investigation_id) if investigation_id else None,
                    decision_type,
                    decision_value,
                    reasoning,
                    confidence,
                    json.dumps(evidence),
                    json.dumps(recommendations),
                    get_optional_tenant_id()
                )

                logger.info(f"[RIGGS] Logged decision: {decision_type} for playbook {playbook_id}")

        except Exception as e:
            # Non-fatal - don't fail the main operation
            logger.warning(f"[RIGGS] Failed to log decision to database: {e}")

