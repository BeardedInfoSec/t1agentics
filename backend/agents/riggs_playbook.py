    async def recommend_integrations(
        self,
        playbook_id: str
    ) -> List[IntegrationRecommendation]:
        """Suggest integrations that would improve the playbook."""
        try:
            from services.postgres_db import postgres_db

            async with postgres_db.tenant_acquire() as conn:
                playbook = await conn.fetchrow(
                    "SELECT * FROM playbooks WHERE id = $1",
                    uuid.UUID(playbook_id)
                )

                if not playbook:
                    return []

                canvas_data = playbook['canvas_data']
                if isinstance(canvas_data, str):
                    canvas_data = json.loads(canvas_data)

                nodes = canvas_data.get('nodes', [])
                recommendations = []

                # Check existing integrations used
                used_integrations = set()
                for node in nodes:
                    config = node.get('data', {}).get('config', {})
                    if 'integration' in config:
                        used_integrations.add(config['integration'])
                    if 'integrations' in config:
                        used_integrations.update(config['integrations'])

                # Recommend based on alert types
                alert_types = playbook.get('alert_types', [])
                tags = playbook.get('tags', [])

                if any('phishing' in t.lower() for t in alert_types + tags):
                    if 'virustotal' not in used_integrations:
                        recommendations.append(IntegrationRecommendation(
                            integration_name="VirusTotal",
                            integration_type="threat_intel",
                            reason="Essential for URL and domain reputation checks in phishing investigations",
                            use_case="Enrich suspicious URLs and domains from phishing emails",
                            node_suggestions=[{"type": "enrich", "config": {"integration": "virustotal"}}]
                        ))
                    if 'urlscan' not in used_integrations:
                        recommendations.append(IntegrationRecommendation(
                            integration_name="URLScan.io",
                            integration_type="threat_intel",
                            reason="Provides screenshots and detailed analysis of suspicious URLs",
                            use_case="Visual inspection of phishing landing pages"
                        ))

                if any('malware' in t.lower() or 'endpoint' in t.lower() for t in alert_types + tags):
                    if 'crowdstrike' not in used_integrations:
                        recommendations.append(IntegrationRecommendation(
                            integration_name="CrowdStrike Falcon",
                            integration_type="edr",
                            reason="Leading EDR for endpoint containment and forensics",
                            use_case="Isolate infected hosts and collect process/file data"
                        ))

                # General recommendations
                if 'slack' not in used_integrations:
                    recommendations.append(IntegrationRecommendation(
                        integration_name="Slack",
                        integration_type="notification",
                        reason="Real-time notifications to SOC team",
                        use_case="Alert analysts when playbook requires attention"
                    ))

                return recommendations

        except Exception as e:
            logger.error(f"Failed to recommend integrations: {e}")
            return []

    async def apply_recommendation(
        self,
        playbook_id: str,
        recommendation: PlaybookRecommendation
    ) -> Dict[str, Any]:
        """Apply a recommendation to a playbook."""
        if recommendation.type == "add_node":
            changes = recommendation.changes
            return await self.add_node(
                playbook_id=playbook_id,
                node_type=changes.get('node_type'),
                config=changes.get('config', {}),
                connect_after=changes.get('after_node'),
                reasoning=recommendation.reasoning
            )
        elif recommendation.type == "update_node":
            changes = recommendation.changes
            return await self.update_node(
                playbook_id=playbook_id,
                node_id=changes.get('node_id'),
                config=changes.get('config', {}),
                reasoning=recommendation.reasoning
            )
        else:
            return {"error": f"Cannot auto-apply recommendation type: {recommendation.type}"}

